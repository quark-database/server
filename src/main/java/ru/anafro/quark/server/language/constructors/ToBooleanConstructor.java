package ru.anafro.quark.server.language.constructors;

import ru.anafro.quark.server.language.constructors.exceptions.ConstructorRuntimeException;
import ru.anafro.quark.server.language.entities.Entity;
import ru.anafro.quark.server.language.entities.EntityConstructor;
import ru.anafro.quark.server.language.entities.InstructionEntityConstructorArguments;
import ru.anafro.quark.server.utils.types.Booleans;

import static ru.anafro.quark.server.language.entities.InstructionEntityConstructorParameter.optional;
import static ru.anafro.quark.server.language.entities.InstructionEntityConstructorParameter.required;
import static ru.anafro.quark.server.language.entities.InstructionEntityConstructorReturnDescription.returns;

/**
 * This class represents the "to boolean" constructor of Quark QL.
 * <br><br>
 * <p>
 * Note that you should not create instances of this class
 * by your own. Instead, use {@code Quark.constructors().get("to boolean"); }
 * to get an instance of this class.
 *
 * <br><br>
 * <p>
 * You can check out the syntax of this constructor by running
 * <pre>
 * {@code
 * Quark.constructors().get("to boolean").getSyntax();
 * }
 * </pre>
 *
 * @author Anatoly Frolov | Анатолий Фролов | <a href="https://anafro.ru">My website</a>
 * @version Quark 1.1
 * @since Quark 1.1
 */
public class ToBooleanConstructor extends EntityConstructor {

    /**
     * Creates a new instance of the "to boolean" constructor
     * representing object.
     * <br><br>
     * <p>
     * Note that you should not create instances of this class
     * by your own. Instead, use Quark.constructors().get("to boolean");
     * to get an instance of this class.
     * <br><br>
     * <p>
     * You can check out the syntax of this constructor by running
     * <pre>
     * {@code
     * Quark.constructors().get("to boolean").getSyntax();
     * }
     * </pre>
     *
     * @author Anatoly Frolov | Анатолий Фролов | <a href="https://anafro.ru">My website</a>
     * @since Quark 1.1
     */
    public ToBooleanConstructor() {
        super(
                "to boolean",

                returns("the boolean", "boolean"),

                required("string to convert to boolean", "str"),
                optional("default boolean value if conversation fails", "boolean")
        );
    }

    /**
     * Invokes the @to boolean constructor programmatically with arguments.
     * <br><br>
     * <p>
     * You can check out the syntax of this constructor by running
     * <pre>
     * {@code
     * Quark.constructors().get("to boolean").getSyntax();
     * }
     * </pre>
     *
     * @param arguments the arguments this constructor needs to be invoked with.
     * @author Anatoly Frolov | Анатолий Фролов | <a href="https://anafro.ru">My website</a>
     * @since Quark 1.1
     */
    @Override
    protected Entity invoke(InstructionEntityConstructorArguments arguments) {
        var string = arguments.getString("string to convert to boolean");

        if (!Booleans.canBeCreatedFromString(string)) {
            arguments.tryGetBoolean("default boolean value if conversation fails").orElseThrow(() -> {
                return new ConstructorRuntimeException(STR."Cannot convert '\{string}' to a boolean.");
            });
        }

        return Entity.wrap(Booleans.createFromString(string));
    }
}
